public class Review055_NestedIfElse {
    public static void main(String[] args){
        int x = 15;
        int y = 31;

        if(x < 16){
            System.out.println("x is less than 16");

            if(y > 30){
                System.out.println("And y is greater than 31");
            }
        }
    }
}
