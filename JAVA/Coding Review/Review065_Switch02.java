public class Review065_Switch02 {
    public static void main(String[] args){
        int day = 4;

        switch (day){
            case 1:
                System.out.println("It's Sunday!");
                break;
            case 7:
                System.out.println("It's Sabbath!");
                break;
            default:
                System.out.println("It's weekdays");
                break;

        }


    }
}
